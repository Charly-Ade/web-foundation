/**
 * variables declaration with var, let and const
 * - var: function-scoped, can be re-declared and updated
 * - let: block-scoped, can be updated but not re-declared
 * - const: block-scoped, cannot be updated or re-declared
 */
 //using var
var name = "Alice"
console.log(name)

//using let
let age = 30
console.log(age)

//using const
const PI = 3.14
console.log(PI)