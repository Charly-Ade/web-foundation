/**
 * a module that uses switch for control structure.
 */
let grade ="A"

switch (grade) {
    case "A":
        console.log("Excellent")
        break;
    case "B":
        console.log("Good") 
        break;
    case "C":
        console.log("credit")
        break;
    case "D":
        console.log("pass")
        break;
    case "E":
        console.log("fair")
        break;
    default:
        console.log("fail")
        break;
}   