/**
 * a module that contains functions for arithmetic operations.
 */

function add(a, b) {
    return a + b
}

function sub(a, b) {
    return a - b
}

function mult(a, b) {
    return a * b
}

function div(a, b) {
    return a / b
}

function mod(a, b) {
    return a % b
}

function pow(a, b) {
    return a ** b
}
console.log(add(2, 3))
console.log(sub(5, 2))
console.log(mult(4, 3))
console.log(div(10, 2))
console.log(mod(10, 3))
console.log(pow(2, 3))    
